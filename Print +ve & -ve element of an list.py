l = [1, -2, 3, -4, 5, -6]
print("Positive elements in the list:")
for i in l:
    if i >= 0:
        print(i)
print("Negative elements in the list:")
for i in l:
    if i < 0:
        print(i)
# Output:
# Positive elements in the list:
# 1
# 3
# 5
# Negative elements in the list:
# -2
# -4
# -6        